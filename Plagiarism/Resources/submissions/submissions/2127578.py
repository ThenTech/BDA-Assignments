Twintig = int(input("Hoeveel munten van 20 cent heb je?"))
Waarde20 = float(0.20)
A20 = Twintig*Waarde20
Tien = int(input("Hoeveel munten van 10 cent heb je?"))
Waarde10 = float(0.10)
A10 = Tien*Waarde10
Vijf = int(input("Hoeveel munten van 5 cent heb je?"))
Waarde5 = float(0.05)
A5 = Vijf*Waarde5
Twee = int(input("Hoeveel munten van 2 cent heb je?"))
Waarde2 = float(0.02)
A2 = Twee*Waarde2
Een = int(input("Hoeveel munten van 1 cent heb je?"))
Waarde1 = float(0.01)
A1 = Een*Waarde1
Totaal = A20 + A10 + A5 + A2 + A1
Cijfer1 = Totaal // 1
C1 = Totaal % 1
Cijfer2 = C1 // 0.1
C2 = C1 % 0.1
Cijfer3 = C2 // 0.01
print("You have ", Cijfer1, ".", Cijfer2, Cijfer3, " euro!", sep="")
